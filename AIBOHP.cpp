void solve() {
 
	string s;
	cin >> s;
 
	string p = s;
	reverse(s.begin(), s.end());
 
	ll dp[s.length() + 1][s.length() + 1];
	memset(dp, 0, sizeof(dp));
 
	for (ll i = 1; i <= s.length(); i++) {
		for (ll j = 1; j <= s.length(); j++) {
			if (s[i - 1] == p[j - 1]) {
				dp[i][j] = 1 + dp[i - 1][j - 1];
			}
			else {
				dp[i][j] = max(dp[i - 1][j], dp[i][j - 1]);
			}
		}
	}
 
	ll m = dp[s.length()][s.length()];
 
	cout << s.length() - m << "\n";
 
 
}
